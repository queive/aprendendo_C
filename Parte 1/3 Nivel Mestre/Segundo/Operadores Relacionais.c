/*
A > B (Maior que)
A < B (Menor que)
A = B (Maior ou igual a)
A <= B (Menor ou igual)
A == B (Igual a)
A != B (Diferente de)
*/
#include <stdio.h>

int main(){
int a = 20;
int b = 20;

printf ("a >= b: %d\n", a >= b);
printf ("a <= b: %d\n", a <= b);
printf ("a > b: %d\n", a > b);
printf ("a < b: %d\n", a < b);
printf ("a == b: %d\n", a == b);
printf ("a != b: %d\n", a != b);
   return 0;
}

